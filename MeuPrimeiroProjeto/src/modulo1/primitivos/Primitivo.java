package modulo1.primitivos;

public class Primitivo {
	// Primitivos inteiros
	byte b = -128;
	short s = -32768;
	int a = 5;
	long l = 10;
	
	// Primitivos ponto flutuante
	float f = 32.5f;
	double d = 9.6;
	
	// Primitivo char
	char c = 'F';
	
	// Primitivo booleano
	boolean bool = false; // true
}
